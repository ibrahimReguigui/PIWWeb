    public function findBynom($txt)
    {
        $entityManager=$this->getEntityManager();
        $query=$entityManager
            ->createQuery('SELECT c FROM App\Entity\Event c WHERE c.nomevent LIKE :txt or c.datedebut LIKE :txt or c.datefin LIKE :txt or c.nbrplaces LIKE :txt or c.location LIKE :txt')
            ->setParameter('txt','%'.$txt.'%');
        return $query->getResult();
